    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        // TODO add your handling code here:
        if (this.txtBuscar.getText().isEmpty())
        {
            llenatblProducto(false, "");
        } else
        {
            llenatblProducto(true, this.txtBuscar.getText());
        }
        int idi;
        idi = this.tblInsumo.getSelectedRow();
        pro.setIdProducto(Integer.parseInt(dtm.getValueAt(idi, 0).toString()));
        pro.setNombreProducto(dtm.getValueAt(idi, 1).toString());

        this.dispose();
    }//GEN-LAST:event_txtBuscarKeyReleased

