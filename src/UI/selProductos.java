    private void llenatblProducto(boolean sw, String Cod) {
        Vector<Producto> listaPro;
        listaPro = proDAO.listaProductos(sw, Cod);
        dtm.setRowCount(0);
        for (int i = 0; i < listaPro.size(); i++)
        {
            Vector vec = new Vector();
            vec.addElement(listaPro.get(i).getIdProducto());
            vec.addElement(listaPro.get(i).getNombreProducto());
            dtm.addRow(vec);
        }

    }
