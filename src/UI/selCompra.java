    private boolean valida(){
        boolean sw = false;
        String aux = "";
        if(this.txtNombreProducto.getText().isEmpty()){
            aux = "Debes de ingresar el nombre";
        }if(this.txtPrecioP.getText().isEmpty()){
            aux += "\nDebes de ingresar el precio";
        }if(this.txtCantidad.getText().isEmpty()){
            aux = "Debes de ingresar la cantidad";
        }if(aux.isEmpty()){
            sw = true;
        }else{
            JOptionPane.showMessageDialog(this, aux);
        }
        return sw;
    }
    public void llenarCosto(){
        String costo = util.obtenerSuma();
        txtTotalCompra.setText(costo);
    }
