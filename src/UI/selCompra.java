    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if(valida()== true){
            Producto prod;
            
            String fechaHoy = util.obtenerFecha();
                    
            prod = new Producto();
            prod.setNombreProducto(this.txtNombreProducto.getText());
            prod.setIdCategoria(Integer.parseInt(this.txtCategoria.getText()));
            prod.setPrecioProducto(Double.parseDouble(this.txtPrecioP.getText()));
            prod.setCantidad(Integer.parseInt(this.txtCantidad.getText()));
            prod.setCostoTotal(Double.parseDouble(this.txtCategoria.getText()));
            prod.setFecha(fechaHoy);
            
            
            
            if(this.btnRegistrar.getText().equals("REGISTRAR")){
               
                this.idProd = util.idNext("Producto", "IdProducto");
                prod.setIdProducto(idProd);
                this.proDAO.procesaItems(prod, "insert");
                
            }else{
                prod.setIdProducto(idProd);
                this.proDAO.procesaItems(prod, "update");
            }
            llenaTblProductos(false, "Tik");
            limpiar();
            llenarCosto();
            
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

