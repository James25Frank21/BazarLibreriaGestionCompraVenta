    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if(valida()== true){
            Categoria cate;
            cate = new Categoria();
            cate.setNameCategoria(this.txtNombreCategoria.getText());
            cate.setDescripcion(this.txtDescripcion.getText());
            
            if(this.btnRegistrar.getText().equals("REGISTRAR")){
                Util util;
                util = new Util();
                this.idCat = util.idNext("Categoria", "IdCategoria");
                cate.setIdCategoria(idCat);
                this.cateDAO.procesaItems(cate, "insert");
                
            }else{
                cate.setIdCategoria(idCat);
                this.cateDAO.procesaItems(cate, "update");
            }
            llenaTblCategoria(false, "Tik");
            limpiar();
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void tblCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriaMouseClicked
        // TODO add your handling code here:
        int idx;
        idx = this.tblCategoria.getSelectedRow();
        this.idCat = Integer.parseInt(dtm.getValueAt(idx, 0).toString());
        this.txtIdCategoria.setText(dtm.getValueAt(idx, 0).toString());
        this.txtNombreCategoria.setText(dtm.getValueAt(idx, 1).toString());
        this.txtDescripcion.setText(dtm.getValueAt(idx, 2).toString());
        
        this.btnRegistrar.setText("Actualizar");
                
    }//GEN-LAST:event_tblCategoriaMouseClicked

